console.log("all between -10 ~ 19");
for (var i = -10; i <= 19; i++) {
    console.log(i);
}

console.log("all even between 10 ~ 40");
for (var a = 10; a <= 40; a++) {
    if (a % 2 == 0) {
        console.log(a);
    }
}

console.log("all odd between 300 ~ 333");
for (b = 5; b <= 50; b++) {
    if (b % 5 == 0 & b % 3 == 0) {
        console.log(b);
    }
}